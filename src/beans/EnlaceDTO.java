package beans;

public class EnlaceDTO {
	private int idRol;
	private String nomRol;
	private int idEnlace;
	private String nomEnlace;
	private String ruta;
	
	public int getIdRol() {
		return idRol;
	}
	public void setIdRol(int idRol) {
		this.idRol = idRol;
	}
	public String getNomRol() {
		return nomRol;
	}
	public void setNomRol(String nomRol) {
		this.nomRol = nomRol;
	}
	public int getIdEnlace() {
		return idEnlace;
	}
	public void setIdEnlace(int idEnlace) {
		this.idEnlace = idEnlace;
	}
	public String getNomEnlace() {
		return nomEnlace;
	}
	public void setNomEnlace(String nomEnlace) {
		this.nomEnlace = nomEnlace;
	}
	public String getRuta() {
		return ruta;
	}
	public void setRuta(String ruta) {
		this.ruta = ruta;
	}
}
